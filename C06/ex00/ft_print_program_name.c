#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	n;

	i = 0;
	n = '\n';
	while (argv[0][i] != '\0' && argc)
	{
		write(1, &argv[0][i], 1);
		i++;
	}
	write (1, &n, 1);
	return (0);
}
