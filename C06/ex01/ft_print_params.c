#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	int		j;
	char	n;

	j = 1;
	i = 0;
	n = '\n';
	while (j < argc)
	{
		i = 0;
		while (argv[j][i] != '\0')
		{
			write (1, &argv[j][i], 1);
			i++;
		}
		write (1, &n, 1);
		j++;
	}
	return (0);
}
